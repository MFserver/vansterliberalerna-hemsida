<p>Grundläggande tandvård och sjukvård skall vara gratis. Vård subventioneras så att läkemedels- och vårdkostnaden inte gör att din månadsinkomst går under socialbidragsnivå. Dock får läkemedelskostnaden inte gå över 1500 kr per år för en enskild individ. 

<p>Om man orsakar sig själv skada, får man själv betala kostnaden för vården. Subventionerat av staten, men inte gratis. Det får dock maximalt kosta individen 12 000 kronor per år. 

<p>Rökning på allmän plats skall förbjudas. Lättare droger, till exempel marijuana, ska legaliseras. Tyngre droger eller droger som utgör fara för andra personer ska fortfarande vara förbjudna, till exempel hasch, anabola steroider, heroin med flera. 

<p>Sjukpenning och socialbidrag ska sluta tätt, man ska inte kunna hamna mellan stolarna. Ansökningen ska vara tillräckligt lätt för att vem som helst ska kunna klara av det utan hjälp eller specialutbildning. Systemet måste raffinieras. 

<p>Att söka socialbidrag eller sjukpenning ska man kunna få hjälp med om man inte klarar det, likaså ska man kunna få hjälp med att ta sig till möten i anslutning till det om man inte klarar det själv. Likaså ska man kunna få hjälp med färdtjänst, både för nöjesresor och för nödvändiga resor. Dock får man betala en större del själv för nöjesresor. 
